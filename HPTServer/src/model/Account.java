package model;

public class Account {
	private String _id;
	private String _name;
	private String _birth;
	private String _address;
	private String _idenNo;
	private String _phone;
	private String _job;
	private String _dateCreate;
	private String _status;
	public String get_id() {
		return _id;
	}
	public void set_id(String _id) {
		this._id = _id;
	}
	public String get_name() {
		return _name;
	}
	public void set_name(String _name) {
		this._name = _name;
	}
	public String get_birth() {
		return _birth;
	}
	public void set_birth(String _birth) {
		this._birth = _birth;
	}
	public String get_address() {
		return _address;
	}
	public void set_address(String _address) {
		this._address = _address;
	}
	public String get_idenNo() {
		return _idenNo;
	}
	public void set_idenNo(String _idenNo) {
		this._idenNo = _idenNo;
	}
	public String get_phone() {
		return _phone;
	}
	public void set_phone(String _phone) {
		this._phone = _phone;
	}
	public String get_job() {
		return _job;
	}
	public void set_job(String _job) {
		this._job = _job;
	}
	public String get_dateCreate() {
		return _dateCreate;
	}
	public void set_dateCreate(String _dateCreate) {
		this._dateCreate = _dateCreate;
	}
	public String get_status() {
		return _status;
	}
	public void set_status(String _status) {
		this._status = _status;
	}
}
